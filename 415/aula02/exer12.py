dia_semana = int(input("Digite o número do dia da semana: "))

if dia_semana == 1:
    print("Domingo")
elif dia_semana == 2:
    print("Segunda")
elif dia_semana == 3:
    print("Terça")
elif dia_semana == 4:
    print("Quarta")
elif dia_semana == 5:
    print("Sexta")
elif dia_semana == 6:
    print("Sábado")
else:
    print("Valor inválido!")